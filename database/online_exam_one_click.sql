-- 15. 批量生成主观题答案（为所有有成绩的学生生成）
-- =====================================================
-- 从paper_manage获取试卷中的主观题(questionType=4)
-- 每卷2道主观题，每题10分，总分20分

-- 先清空旧的主观题答案数据
DELETE FROM essay_answer;

-- 为所有考试的学生批量生成主观题答案（仅限paper_manage中定义的主观题）
INSERT INTO essay_answer (examCode, studentId, questionId, studentAnswer, aiScore, aiComment, gradingStatus, gradingTime)
SELECT DISTINCT s.examCode, s.studentId, pm.questionId,
    CASE 
        WHEN s.etScore > 64 THEN CONCAT('这是一道关于', SUBSTRING(eq.question, 1, 20), '的问题。我的详细回答是：该知识点涉及核心概念和实际应用，需要从理论和实践两个角度进行分析。首先从定义上理解，然后通过具体案例说明其应用场景和注意事项。')
        WHEN s.etScore > 48 THEN CONCAT('关于', SUBSTRING(eq.question, 1, 15), '，我的回答是：这是一个重要的知识点，主要包含几个关键要素。')
        ELSE CONCAT('回答：', SUBSTRING(eq.question, 1, 10))
    END as studentAnswer,
    CASE WHEN s.etScore > 64 THEN FLOOR(eq.score * 0.85) 
         WHEN s.etScore > 48 THEN FLOOR(eq.score * 0.6) 
         ELSE FLOOR(eq.score * 0.3) END as aiScore,
    '基于答案完整度和准确性自动评分' as aiComment,
    'ai_graded' as gradingStatus,
    NOW() as gradingTime
FROM score s
INNER JOIN exam_manage em ON s.examCode = em.examCode
INNER JOIN paper_manage pm ON em.paperId = pm.paperId AND pm.questionType = 4
INNER JOIN essay_question eq ON pm.questionId = eq.questionId;

-- 先将etScore按比例调整到0-80分范围（客观题满分80分）
UPDATE score SET etScore = FLOOR(etScore * 0.8) WHERE etScore > 80;

-- 更新score表中的ptScore（主观题总分，满分20分）
UPDATE score sc SET ptScore = (
    SELECT IFNULL(SUM(IFNULL(ea.aiScore, 0)), 0)
    FROM essay_answer ea 
    WHERE ea.examCode = sc.examCode AND ea.studentId = sc.studentId
);

